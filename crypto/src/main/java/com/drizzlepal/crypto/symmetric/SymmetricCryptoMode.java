/**
 * 对称加密模式枚举类
 * 提供了一系列常用的对称加密模式，包括ECB、CBC、CFB、OFB和CTR
 * 这些模式用于定义在对称加密算法中如何处理数据块
 */
package com.drizzlepal.crypto.symmetric;

/**
 * 定义了对称加密算法中使用的不同模式
 * ECB（电子密码本模式）：对每一数据块独立地进行加密或解密，适用于少量数据
 * CBC（密码块链模式）：每个数据块在加密之前与前一个密文块进行异或，增加了数据的依赖性
 * CFB（密码反馈模式）：将前一个密文块作为输入到加密算法中，输出与当前明文块异或得到密文块
 * OFB（输出反馈模式）：与CFB类似，但反馈的是加密算法的输出，而不是密文块
 * CTR（计数器模式）：使用一个计数器作为加密算法的输入，增加了并行处理的能力
 */
public enum SymmetricCryptoMode {
    ECB,
    CBC,
    CFB,
    OFB,
    CTR
}